void	cast_map(int tab[20][20], t_env *screen)
{
	int		index;
	int		count;

	index = 0;
	while (index < 20)
	{
		count = 0;
		while (count < 20)
		{
			screen->tab[index][count] = tab[index][count];
			count++;
		}
		index++;
	}
}

